package sukkiriNyumon.chapter1_03;

public class Code07_For {
	public static void main(String[] args) {
		
		//基本形
		for (int i = 0; i <  5; i++) {
			System.out.println(i);
		}
		//数字を1から出力したい場合
		for (int i = 1; i <= 5; i++) {
			System.out.println(i);
		}
		
		//どちらも5回繰り返しになる。
	}
}
